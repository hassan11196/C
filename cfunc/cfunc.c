char* cfuncv5_prime(int num){
	for(i=2;i<=num;i++){
		if(num%i==0&&i<num){
			return "NO";
		}
		else if(num%i==0&&num==i){
			return "YES";
		}
	}
}
char* cfuncv5_armstrong(int num){
	int tnum=num,digits=0,pow_num=1,sum=0;
	while(tnum!=0){
		tnum/=10;
		digits++;
	}
	tnum=num;
	while(tnum!=0){
		pow_num=1;
		for(j=1;j<=digits;j++){
			pow_num= pow_num * (tnum%10);
		}
		sum = sum + pow_num;
		tnum/=10;
	}
	if(sum==num)
	return "YES";
	else
	return "NO";
}
